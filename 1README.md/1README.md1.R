# Introducción: -----------------------------------------------------------

# La materia de Experimentación y Métodos Estadísticos tuvo como propósito 
# desarrollar competencias en el manejo, análisis e interpretación de datos mediante 
# el uso de herramientas estadísticas y computacionales. Para ello, se empleó el 
# lenguaje de programación R, reconocido por su amplia aplicación en la investigación 
# científica, el análisis estadístico y la visualización de datos.

# Como parte de las actividades iniciales del curso, se realizó la instalación y 
# configuración de Git y GitHub, herramientas fundamentales para la gestión de 
# proyectos de programación. Asimismo, se configuraron las credenciales de usuario 
# en el equipo de cómputo con el fin de establecer la sincronización entre el 
# repositorio local y remoto, permitiendo almacenar, respaldar y actualizar los 
# scripts desarrollados durante las clases.

# Posteriormente, se abordaron diversos métodos de estadística paramétrica, 
# comenzando con el análisis de estadística descriptiva y la evaluación de los 
# supuestos necesarios para la aplicación de pruebas inferenciales. En esta 
# etapa se emplearon pruebas de normalidad, como la prueba de Shapiro-Wilk, 
# y pruebas de homogeneidad de varianzas, las cuales permitieron verificar la 
# adecuación de los datos para análisis estadísticos posteriores.

# Dentro de los análisis de asociación entre variables, se estudiaron diferentes 
# métodos de correlación, incluyendo la correlación de Spearman, 
# la correlación Tau de Kendall y la correlación punto biserial, herramientas 
# que permitieron evaluar la intensidad y dirección de las relaciones existentes
# entre variables cuantitativas y cualitativas bajo distintos escenarios de análisis.

# Asimismo, se desarrollaron procedimientos para la comparación de medias 
# mediante pruebas t de Student. Entre ellas se aplicó la prueba t para una 
# muestra, utilizando el índice de esbeltez como valor de referencia o media 
# conocida, así como la prueba t para muestras dependientes, empleada para 
# comparar mediciones relacionadas y determinar la existencia de diferencias 
# estadísticamente significativas entre ellas.

# Finalmente, se introdujo el análisis de regresión lineal simple, utilizado
# para modelar la relación entre variables y generar ecuaciones predictivas. 
# Como parte de la validación de los modelos obtenidos, se aplicó la prueba de
# Breusch-Pagan para evaluar la homogeneidad de la varianza de los 
# residuos (homocedasticidad), además de realizar un análisis de la varianza 
# residual con el propósito de verificar el cumplimiento de los supuestos 
# estadísticos necesarios para la correcta interpretación de los resultados.

# En conjunto, las actividades desarrolladas durante el curso permitieron 
# fortalecer las habilidades en programación estadística, análisis de datos y 
# aplicación de métodos inferenciales, proporcionando herramientas fundamentales 
# para la investigación experimental y la toma de decisiones basada en 
# evidencia cuantitativa.

